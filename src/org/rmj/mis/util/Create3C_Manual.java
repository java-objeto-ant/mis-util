    public static void main (String [] args){
        LogWrapper logwrapr = new LogWrapper("3C", "recalc.log");
        
        String path;
        if(System.getProperty("os.name").toLowerCase().contains("win")){
            path = "D:/GGC_Java_Systems";
        }
        else{
            path = "/srv/mac/GGC_Java_Systems";
        }
        System.setProperty("sys.default.path.config", path);
        
        GRiderX instance = new GRiderX("gRider");
        
        if(!instance.getErrMsg().isEmpty()){
            logwrapr.severe(instance.getMessage() + instance.getErrMsg());
            System.exit(1);
        }
        
        ArrayList<String> accounts = getAccounts();
        
        for (int lnCtr = 0; lnCtr <= accounts.size() - 1; lnCtr++){
            instance.beginTrans();
            
            //get selling branch of the account
            String lsSQL = "SELECT sBranchCd FROM MC_AR_Master WHERE sAcctNmbr = " + SQLUtil.toSQL(accounts.get(lnCtr));
            ResultSet loRS = instance.executeQuery(lsSQL);
            
            try {
                if (loRS.next()){
                    lsSQL = "DELETE FROM LR_Collection_Unit WHERE sAcctNmbr = " + SQLUtil.toSQL(accounts.get(lnCtr));
                    System.out.println(lsSQL);
                    instance.executeUpdate(lsSQL);

                    //create for collection
                    lsSQL = "INSERT INTO LR_Collection_Unit SET" +
                            "  sAcctNmbr = " + SQLUtil.toSQL(accounts.get(lnCtr)) +
                            ", sBranchCd = " + SQLUtil.toSQL(loRS.getString("sBranchCd")) +
                            ", dTransact = " + SQLUtil.toSQL(instance.getServerDate()) +
                            ", cCollUnit = '0'" +
                            ", sInCharge = " + SQLUtil.toSQL("") +
                            ", cApntUnit = " + SQLUtil.toSQL("") +
                            ", dDueUntil = " + SQLUtil.toSQL(CommonUtils.dateAdd(instance.getServerDate(), 5)) +
                            ", cCollStat = '0'" +
                            ", nPriority = 1" + 
                            ", nNoSchedx = 0" +
                            ", sModified = 'M001111122'" + 
                            ", dModified = " + SQLUtil.toSQL(instance.getServerDate());

                    System.out.println(lsSQL);
                    if (instance.executeUpdate(lsSQL) <= 0){
                        instance.rollbackTrans();
                        System.err.println(instance.getErrMsg() + "; " + instance.getMessage());
                        System.exit(1);
                    }

                    //add customer to leads
                    lsSQL = "INSERT INTO LR_Calls_Master SET" +
                            "  sTransNox = " + SQLUtil.toSQL(MiscUtil.getNextCode("LR_Calls_Master", "sTransNox", true, instance.getConnection(), instance.getBranchCode())) +
                            ", dTransact = " + SQLUtil.toSQL(instance.getServerDate()) +
                            ", sAcctNmbr = " + SQLUtil.toSQL(accounts.get(lnCtr)) +
                            ", sRemarksx = ''" +
                            ", cTranStat = '0'" +
                            ", sAsgAgent = ''" +
                            ", sAgentIDx = ''" +
                            ", sModified = 'M001111122'" + 
                            ", dModified = " + SQLUtil.toSQL(instance.getServerDate());

                    System.out.println(lsSQL);
                    if (instance.executeUpdate(lsSQL) <= 0){
                        instance.rollbackTrans();
                        System.err.println(instance.getErrMsg() + "; " + instance.getMessage());
                        System.exit(1);
                    }

                    //update for collection
                    lsSQL = "UPDATE LR_Collection_Unit SET" +
                                "  dScheduld = " + SQLUtil.toSQL(instance.getServerDate()) +
                                ", sInCharge = ''" +
                                ", cCollStat = '1'" + 
                            " WHERE sAcctNmbr = " + SQLUtil.toSQL(accounts.get(lnCtr)) +
                                " AND cCollUnit = '0'";

                    System.out.println(lsSQL);
                    instance.executeUpdate(lsSQL);
                }
            } catch (SQLException e) {
                instance.rollbackTrans();
                e.printStackTrace();
                System.exit(1);
            }
            
            instance.commitTrans();
        }

        System.exit(0);
    }
    
